function createBoxes (array){
    containerHTML.innerHTML='';
    array.forEach(el => {
    const boxHTML = document.createElement('div');
    boxHTML.className=('box col-2 me-1 mb-4 py-2');
    boxHTML.innerHTML= `
        <p class="${el.family} ${el.prefix}${el.name} ${el.color}"></p>
        <p>${el.name}</p>`;
    containerHTML.append(boxHTML);
    });   
}

function filteredArray (color){
    const filtered = iconArray.filter(el => el.team === `${color}`);
    return filtered;    
}

const containerHTML = document.querySelector('.icon');
const selectBtn = document.getElementById('filter-icon');

createBoxes (iconArray);

selectBtn.addEventListener('change', (event) =>{
    containerHTML.innerHTML='';
    const filtered = iconArray.filter(el => el.color === `${event.target.value}`);
    createBoxes (filtered);
  
    if (event.target.value==='all'){
        createBoxes (iconArray);
    }
});